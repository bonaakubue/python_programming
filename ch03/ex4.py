# Relational Operators

# Greater than
value1 = 7
value2 = 4
result1 = value1 > value2
print("Greater than:", result1)

# Less than
value3 = 8
value4 = 5
result2 = value3 < value4
print("Less than:", result2)

# Greater than or equal to 
value5 = 5
value6 = 5
result3 = value5 >= value6
print("Greater than or equal to:", result3)

# Less than or equal to 
value7 = 9
value8 = 9
result4 = value7 <= value8
print("Less than or equal to:", result4)

#equality
value9 = 4
value10 = 7
result5 = value9 == value10
print("Equal to:", result5)

#not equality
value11 = 3
value12 = 2
result6 = value11 == value12
print("Equal to:", result6)
