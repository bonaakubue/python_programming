# Removing items from a list
#pop method
fruits = ['orange', 'strawberry', 'apple', 'grape']
fruit = fruits.pop()

print(fruits)
print(fruit)

#pop method with argument
fruits = ['orange', 'strawberry', 'apple', 'grape']
fruit = fruits.pop(2)

print(fruits)

#remove method
fruits = ['orange', 'strawberry', 'apple', 'grape']
fruits.remove('apple')
print(fruits)

