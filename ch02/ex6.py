# Random Numbers

# randint()
from random import randint
num = randint(1,5)
print(num)


# random()
from random import random
num = random()
print(num)


# choice()
from random import choice
L = ['apple', 'grape', 'mango', 'banana']
result = choice(L)
print(result)


# randrange()
from random import randrange

result = randrange(1,10,2)
print(result)

